def addition(num1, num2):
    num3 = num1 + num2
    print(num3)

def subtraction(num1, num2):
    num3 = num1 - num2
    print(num3)

def division(num1, num2):
    num3 = num1 / num2
    print(num3)

def multiplication(num1, num2):
    num3 = num1 * num2
    print(num3)

def modulus(num1, num2):
    num3 = num1 % num2
    print(num3)